var n = prompt("Введите трехзначное число");
if (n.length == 3) {
    if (n[1] == n[2] || n[2] == n[3]) {
        alert("3 символа одинаковые");
    }
    else if (n[1] == n[2] || n[2] == n[3] || n[1] == n[3]) {
        alert("2 символа одинаковые")
    }
    else {
        alert("СОвпадений не обнаружено =/");
    }
}
else {
    alert("Вы ввели" + " " + n.length + "-значное число")
}